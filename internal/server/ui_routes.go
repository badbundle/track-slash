		r.Get("/{owner}/projects/{key}/about", func(w http.ResponseWriter, r *http.Request) { s.uiProjectWorkPage(w, r, "about") })
		r.Get("/{owner}/projects/{key}/about/panel", func(w http.ResponseWriter, r *http.Request) { s.uiProjectWorkPanel(w, r, "about") })
		r.Post("/{owner}/projects/{key}/image", s.uiUpdateProjectImage)
		r.Post("/{owner}/projects/{key}/image/delete", s.uiDeleteProjectImage)
		r.Get("/{owner}/projects/{key}/image/content", s.uiGetProjectImageContent)
		r.Get("/{owner}/projects/{key}/image/thumbnail/content", s.uiGetProjectImageThumbnailContent)
		r.Get("/{owner}/projects/{key}/tags", s.uiProjectTagsPage)
		r.Post("/{owner}/projects/{key}/tags", s.uiProjectWriteHandler(s.uiCreateProjectTag))
		r.Get("/{owner}/projects/{key}/tags/{tagRef}/edit", s.uiProjectWriteHandler(s.uiEditProjectTag))
		r.Post("/{owner}/projects/{key}/tags/{tagRef}", s.uiProjectWriteHandler(s.uiUpdateProjectTag))
		r.Post("/{owner}/projects/{key}/tags/{tagRef}/delete", s.uiProjectWriteHandler(s.uiDeleteProjectTag))
		r.Get("/{owner}/projects/{key}/context", s.uiProjectContextPage)
		r.Get("/{owner}/projects/{key}/context/panel", func(w http.ResponseWriter, r *http.Request) { s.uiProjectWorkPanel(w, r, "context") })
		r.Get("/{owner}/projects/{key}/context/new", s.uiProjectWriteHandler(s.uiNewProjectContext))
		r.Post("/{owner}/projects/{key}/context", s.uiProjectWriteHandler(s.uiCreateProjectContext))
		r.Get("/{owner}/projects/{key}/context/{contextRef}", s.uiViewProjectContext)
		r.Get("/{owner}/projects/{key}/context/{contextRef}/panel", s.uiViewProjectContext)
		r.Get("/{owner}/projects/{key}/context/{contextRef}/edit", s.uiProjectWriteHandler(s.uiEditProjectContext))
		r.Post("/{owner}/projects/{key}/context/{contextRef}", s.uiProjectWriteHandler(s.uiUpdateProjectContext))
		r.Post("/{owner}/projects/{key}/context/{contextRef}/delete", s.uiProjectWriteHandler(s.uiDeleteProjectContext))
		r.Post("/{owner}/projects/{key}/context/{contextRef}/move-up", s.uiProjectWriteHandler(s.uiMoveProjectContextUp))
		r.Post("/{owner}/projects/{key}/context/{contextRef}/move-down", s.uiProjectWriteHandler(s.uiMoveProjectContextDown))
		r.Post("/{owner}/projects/{key}/context/{contextRef}/attachments", s.uiProjectWriteHandler(s.uiCreateContextAttachment))
		r.Get("/{owner}/projects/{key}/context/{contextRef}/attachments/{objectRef}/content", s.uiGetContextAttachmentContent)
		r.Post("/{owner}/projects/{key}/context/{contextRef}/attachments/{objectRef}/delete", s.uiProjectWriteHandler(s.uiDeleteContextAttachment))
		r.Delete("/{owner}/projects/{key}/context/{contextRef}/attachments/{objectRef}", s.uiProjectWriteHandler(s.uiDeleteContextAttachmentJSON))
		r.Get("/{owner}/projects/{key}/context/{contextRef}/issues/new", s.uiProjectWriteHandler(s.uiNewProjectContextIssueLink))
		r.Post("/{owner}/projects/{key}/context/{contextRef}/issues", s.uiProjectWriteHandler(s.uiCreateProjectContextIssueLink))
		r.Post("/{owner}/projects/{key}/context/{contextRef}/issues/{issueRef}/delete", s.uiProjectWriteHandler(s.uiDeleteProjectContextIssueLink))
		r.Get("/{owner}/projects/{key}/name/edit", s.uiProjectWriteHandler(s.uiEditProjectName))
		r.Post("/{owner}/projects/{key}/name", s.uiProjectWriteHandler(s.uiUpdateProjectName))
		r.Post("/{owner}/projects/{key}/favorite", s.uiToggleProjectFavorite)
		r.Get("/{owner}/projects/{key}/members", func(w http.ResponseWriter, r *http.Request) { s.uiProjectWorkPage(w, r, "members") })
		r.Get("/{owner}/projects/{key}/members/panel", func(w http.ResponseWriter, r *http.Request) { s.uiProjectWorkPanel(w, r, "members") })
		r.Post("/{owner}/projects/{key}/members", s.uiAddProjectMember)
		r.Post("/{owner}/projects/{key}/members/{username}", s.uiUpdateProjectMember)
		r.Post("/{owner}/projects/{key}/members/{username}/delete", s.uiDeleteProjectMember)
		r.Get("/{owner}/projects/{key}/description/edit", s.uiProjectWriteHandler(s.uiEditProjectDescription))
		r.Post("/{owner}/projects/{key}/description", s.uiProjectWriteHandler(s.uiUpdateProjectDescription))
		r.Post("/{owner}/projects/{key}/attachments", s.uiProjectWriteHandler(s.uiCreateProjectAttachment))
		r.Get("/{owner}/projects/{key}/attachments/{objectRef}/content", s.uiGetProjectAttachmentContent)
		r.Post("/{owner}/projects/{key}/attachments/{objectRef}/delete", s.uiProjectWriteHandler(s.uiDeleteProjectAttachment))
		r.Delete("/{owner}/projects/{key}/attachments/{objectRef}", s.uiProjectWriteHandler(s.uiDeleteProjectAttachmentJSON))
		r.Get("/{owner}/projects/{key}/sprints", func(w http.ResponseWriter, r *http.Request) { s.uiProjectWorkPage(w, r, "sprints") })
		r.Get("/{owner}/projects/{key}/sprints/panel", func(w http.ResponseWriter, r *http.Request) { s.uiProjectWorkPanel(w, r, "sprints") })
		r.Get("/{owner}/projects/{key}/sprints/page", s.uiProjectSprintHistoryPage)
		r.Get("/{owner}/projects/{key}/sprints/new", s.uiProjectWriteHandler(s.uiNewProjectSprint))
		r.Post("/{owner}/projects/{key}/sprints", s.uiProjectWriteHandler(s.uiCreateProjectSprint))
		r.Get("/{owner}/projects/{key}/sprints/{sprintRef}/history/issues", s.uiProjectSprintHistoryIssues)
		r.Get("/{owner}/projects/{key}/sprints/{sprintRef}/edit", s.uiProjectWriteHandler(s.uiEditProjectSprint))
		r.Get("/{owner}/projects/{key}/sprints/{sprintRef}/description", s.uiProjectSprintDescription)
		r.Post("/{owner}/projects/{key}/sprints/{sprintRef}", s.uiProjectWriteHandler(s.uiUpdateProjectSprint))
		r.Post("/{owner}/projects/{key}/sprints/{sprintRef}/attachments", s.uiProjectWriteHandler(s.uiCreateSprintAttachment))
		r.Get("/{owner}/projects/{key}/sprints/{sprintRef}/attachments/{objectRef}/content", s.uiGetSprintAttachmentContent)
		r.Post("/{owner}/projects/{key}/sprints/{sprintRef}/attachments/{objectRef}/delete", s.uiProjectWriteHandler(s.uiDeleteSprintAttachment))
		r.Delete("/{owner}/projects/{key}/sprints/{sprintRef}/attachments/{objectRef}", s.uiProjectWriteHandler(s.uiDeleteSprintAttachmentJSON))
		r.Post("/{owner}/projects/{key}/sprints/{sprintRef}/activate", s.uiProjectWriteHandler(s.uiActivateProjectSprint))
		r.Post("/{owner}/projects/{key}/sprints/{sprintRef}/complete", s.uiProjectWriteHandler(s.uiCompleteProjectSprint))
		r.Post("/{owner}/projects/{key}/sprints/{sprintRef}/delete", s.uiProjectWriteHandler(s.uiDeleteProjectSprint))
		r.Post("/{owner}/projects/{key}/sprints/{sprintRef}/move-up", s.uiProjectWriteHandler(s.uiMoveProjectSprintUp))
		r.Post("/{owner}/projects/{key}/sprints/{sprintRef}/move-down", s.uiProjectWriteHandler(s.uiMoveProjectSprintDown))
		r.Get("/{owner}/projects/{key}/sprints/{sprintRef}/issues/new", s.uiProjectWriteHandler(s.uiNewProjectSprintIssue))
		r.Post("/{owner}/projects/{key}/sprints/{sprintRef}/issues", s.uiProjectWriteHandler(s.uiAddProjectSprintIssue))
		r.Get("/{owner}/projects/{key}/issues/new", s.uiProjectWriteHandler(s.uiNewProjectIssuePage))
		r.Get("/{owner}/projects/{key}/issues/new/panel", s.uiProjectWriteHandler(s.uiNewProjectIssuePanel))
		r.Post("/{owner}/projects/{key}/issues", s.uiProjectWriteHandler(s.uiCreateProjectIssue))
		r.Get("/{owner}/projects/{key}/sprint", func(w http.ResponseWriter, r *http.Request) { s.uiProjectWorkPage(w, r, "sprint") })
		r.Get("/{owner}/projects/{key}/sprint/panel", func(w http.ResponseWriter, r *http.Request) { s.uiProjectWorkPanel(w, r, "sprint") })
		r.Get("/{owner}/projects/{key}/planned", func(w http.ResponseWriter, r *http.Request) { s.uiProjectWorkPage(w, r, "planned") })
