		r.Get("/{owner}/projects/{key}/changelog/panel", func(w http.ResponseWriter, r *http.Request) { s.uiProjectWorkPanel(w, r, "changelog") })
		r.Get("/{owner}/projects/{key}/changelog/page", s.uiProjectChangelogPage)
